#include "libft.h"

int	ft_strncmp(char *str1, char *str2, int chNum)
{
	int	i;

	i = 0;
	while (i < chNum)
	{
		if (str1[i] != str2[i])
			return (str1[i] - str2[i]);
		i++;
	}
	return (0);
}
